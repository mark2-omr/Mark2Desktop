using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Mark2
{
    class Square
    {
        public int x;
        public int y;
        public int w;
        public int h;
        public int cx;
        public int cy;

        public Square(int x, int y, int w, int h, int cx, int cy)
        {
            this.x = x;
            this.y = y;
            this.w = w;
            this.h = h;
            this.cx = cx;
            this.cy = cy;
        }
    }
}
